
# Пример строк
# Строки в Python — это последовательность символов, заключенных в одинарные ('),
# двойные (") или тройные кавычки (''' или """).
#Они могут содержать текст, числа и даже специальные символы.
string1 = "Привет, мир!"  # Двойные кавычки
string2 = 'Python — круто!'  # Одинарные кавычки
string3 = """Многострочная
строка"""  # Тройные кавычки

#Конкатенация (объединение строк)
name = "Ильяс"
greeting = "Привет, " + name + "!"
print(greeting)  # Вывод: Привет, Ильяс!
#Повторение строки
text = "Python "
print(text * 3)  # Вывод: Python Python Python
#Индексация и срезы
word = "Python"

# Индексация
print(word[0])  # Вывод: P (первый символ)
print(word[-1])  # Вывод: n (последний символ)

# Срезы
print(word[0:3])  # Вывод: Pyt (символы с 0 до 2)
print(word[2:])  # Вывод: thon (с 3 символа до конца)

#Длина строки
message = "Программирование"
print(len(message))  # Вывод: 16 (длина строки)
#Полезные методы для работы со строками
#Метод	Описание	Пример
#.lower()	Преобразует строку в нижний регистр	"PYTHON".lower() -> "python"
#.upper()	Преобразует строку в верхний регистр	"python".upper() -> "PYTHON"
#.capitalize()	Делает первую букву строки заглавной	"python".capitalize() -> "Python"
#.strip()	Удаляет пробелы в начале и конце строки	" hello ".strip() -> "hello"
#.replace(a, b)	Заменяет подстроку a на b	"hello world".replace("world", "Python") -> "hello Python"
#.split()	Разбивает строку на список по разделителю	"a,b,c".split(",") -> ["a", "b", "c"]
#.join()	Соединяет список строк с указанным разделителем	",".join(["a", "b", "c"]) -> "a,b,c"

