
def circle_square(diametr):
    pi = 3.14
    radius = diametr/2
    calculate_circle_square = radius * radius * pi
    print(f"Площадь круга с диаметром: {diametr} = {calculate_circle_square}")

circle_square(20)
circle_square(155)
circle_square(3.33333)
circle_square(188)

