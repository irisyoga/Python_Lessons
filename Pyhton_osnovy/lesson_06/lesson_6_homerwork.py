
#Метод, который рассчитывает периметр квадрата.
# Исходные данные 1:
def calculate_perimeter_square(side_lenght):  #Функция принимает один аргумент,
                                              #который задает длину квадрата
    perimeter = 4 * side_lenght               #Формула для расчета периметра
    print(f"Периметр квадрата со стороной {side_lenght}: {perimeter}") #Выводим информацию
    return perimeter                                                   #Возвращаем информацию
#Пример использования:
calculate_perimeter_square(2)

