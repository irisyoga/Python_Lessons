# Возраст в месяцах:
# В записях метод age_in_months, который принимает возраст пользователя в качестве аргумента.
# Метод должен определять и возвращать возраст в месяцах.
# Формула: Возраст в месяцах = Возраст в годах * 12.

#Метод, который принимает возраст пользователя в качестве аргумента.
# Исходные данные 3:
def age_in_months(age_years):           #Функция принимает один аргумент
    return  age_years * 12              #Возвращаем информацию
 # Пример использования:
result = age_in_months(20)
# Вывод результата:
print(f"{20} years = {result} months")

