"""
Самостоятельная работа. Даны два списка строк – ["A", "B", "C", "D", "E"] и ["D", "E", "F", "G"].
Напишите функцию, которая возвращает третий список, содержащий все элементы обоих списков.
 Ожидаемый результат - ["A", "B", "C", "D", "E", "D", "E", "F", "G"].
"""

list_1 = ["A", "B", "C", "D", "E"]
list_2 = ["D", "E", "F", "G"]

def merge_lists (list_1, list_2):
    return list_1 + list_2                   #Операция сложения объединяет списки

result = merge_lists (list_1, list_2)
print(result)
